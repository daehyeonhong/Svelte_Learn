<script>
    import {onMount} from 'svelte';

    let name = 'World';
    let isRed = false;
    onMount(() => {
        const box = document.querySelector('.box');
        box.addEventListener('click', () => isRed = !isRed);
    });
</script>
<h1>Hello, {name}!</h1>
<div class="box" style="background-color: {isRed?'red':'orange'}">Box!</div>
<style>
    .box {
        width: 300px;
        height: 150px;
        background-color: orange;
    }
</style>